"""Practical 4: Arithmetic, Comparison, and Logical Operations"""

num1 = 10
num2 = 3

# Arithmetic operations
print("Addition:", num1 + num2)
print("Subtraction:", num1 - num2)
print("Multiplication:", num1 * num2)
print("Division:", num1 / num2)

# Comparison operations
print("Equal:", num1 == num2)
print("Greater:", num1 > num2)

# Logical operations
print("AND:", (num1 > 5 and num2 < 5))
print("OR:", (num1 > 5 or num2 > 5))

# Assignment operation with augmented assignment
num1 += 5
print("After += :", num1)