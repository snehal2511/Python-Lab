"""Practical 3: Python Data Types Exploration"""

# Sample variables of common Python data types
int_val = 10           # Integer
float_val = 3.14       # Float
str_val = "Hello"      # String
bool_val = True        # Boolean
none_val = None        # NoneType

# Display each value with its type
print(int_val, type(int_val))
print(float_val, type(float_val))
print(str_val, type(str_val))
print(bool_val, type(bool_val))
print(none_val, type(none_val))
print()